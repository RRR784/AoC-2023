let encode = (x, y) => (x << 8) + y;
let decode = id => [id >> 8, id & 255]

let nodes = {};

let make_node = (x, y) => {
	let id = encode(x, y)
	let node = nodes[id] = nodes[id] || {id, connections:{}}
	return node;
}
let add_connection = (x0, y0, x1, y1, distance) => {
	let node0 = make_node(x0, y0)
	let node1 = make_node(x1, y1)
	node0.connections[node1.id] = distance;
	node1.connections[node0.id] = distance;
}

