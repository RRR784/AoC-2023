let get_combinations = (n, k) => {
	let results = [];
	for(let i = 0; i < Math.pow(n, k); i++)
	{
		let result = [];
		for(let j = 0; j < n; j++)
			result[j] = 0;

		let ix = [];
		for (let j = 0; j < k; j++)
		{
			ix.push(Math.floor(i / Math.pow(n, j)) % n);
		}
		let ok = true;
		for(let j = ix.length; j >= 1; j--)
		{
			for(let d = j - 1; d >= 0; d--)
			{
				if(ix[d] < ix[j])
				{
					debugger;
					let inc = Math.pow(n, d) * (ix[j] - ix[d]);
					i += inc;
					ix[d] = ix[j];
				}
			}
		}
		if (ok)
		{
			for (let j = 0; j < k; j++)
			{
				result[ix[j]]++
			}
			results.push([...result]);
		}
	}
	return results;
}

